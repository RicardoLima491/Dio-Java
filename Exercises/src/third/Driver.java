package third;

public class Driver {
	protected int gear;
		
	protected int speed;	
	
	protected boolean state;

	protected String turnLeftRight;

	public int getGear() {
		return gear;
	}

	public void setGear(int gear) {
		this.gear = gear;
	}

	public int getSpeed() {
		return speed;
	}

	public void setSpeed(int speed) {
		this.speed = speed;
	}

	public boolean isState() {
		return state;
	}

	public void setState(boolean state) {
		this.state = state;
	}

	public String getTurnLeftRight() {
		return turnLeftRight;
	}

	public void setTurnLeftRight(String turnLeftRight) {
		this.turnLeftRight = turnLeftRight;
	}
	
	
		
	
}
